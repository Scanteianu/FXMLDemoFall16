package fxmldemofall16;

import java.util.ArrayList;
import javafx.application.Application;
import javafx.fxml.FXMLLoader;
import javafx.scene.Parent;
import javafx.scene.Scene;
import javafx.stage.Stage;

/**
 *
 * @author Dan
 */
public class FXMLDemoFall16 extends Application {
    private static ArrayList<String> myList=new ArrayList<String>();

    public static ArrayList<String> getMyList() {
        return myList;
    }

    public static void setMyList(ArrayList<String> myList) {
        FXMLDemoFall16.myList = myList;
    }
    @Override
    public void start(Stage stage) throws Exception {
        Parent root = FXMLLoader.load(getClass().getResource("FXMLDocument.fxml"));
        
        Scene scene = new Scene(root);
        
        stage.setScene(scene);
        stage.show();
    }

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        launch(args);
    }
    
}
